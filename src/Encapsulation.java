public class Encapsulation {

    private double I,R,U;


    public void setR(double r) {
        this.R = r;
    }

    public void setI(double i) {
        this.I = i;
    }

    public void setU(double u) {
        this.U = u;
    }

    public double getI() {
        return I;
    }

    public double getR() {
        return R;
    }

    public double getU() {
        return U;
    }
}
